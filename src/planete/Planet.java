package planete;

public class Planet {
    private String nom;
    private int nbPlut;
    private int nbThul;
    private int nbGad;
    private int nbTer;
    private int nbNep;

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getNbPlut() {
        return nbPlut;
    }

    public void setNbPlut(int nbPlut) {
        this.nbPlut = nbPlut;
    }

    public int getNbThul() {
        return nbThul;
    }

    public void setNbThul(int nbThul) {
        this.nbThul = nbThul;
    }

    public int getNbGad() {
        return nbGad;
    }

    public void setNbGad(int nbGad) {
        this.nbGad = nbGad;
    }

    public int getNbTer() {
        return nbTer;
    }

    public void setNbTer(int nbTer) {
        this.nbTer = nbTer;
    }

    public int getNbNep() {
        return nbNep;
    }

    public void setNbNep(int nbNep) {
        this.nbNep = nbNep;
    }
}
